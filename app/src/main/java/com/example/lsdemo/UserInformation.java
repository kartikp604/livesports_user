package com.example.lsdemo;

public class UserInformation {

    public String name;
    public String pen;
    public String mobile;
    public String batch;
    public String department;
    public String gender;
    public String userType;


    public UserInformation(String name, String pen,String mobile, String batch, String dept,String gender,String userType) {




        this.name = name;
        this.pen = pen;
        this.mobile = mobile;
        this.batch = batch;
        this.department=dept;
        this.gender=gender;
        this.userType=userType;
    }
}
